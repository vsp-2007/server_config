#!/bin/bash
# Nginx Reverse Proxy Module - Pi Server Setup v2
# Local domain routing with Pi-hole integration

set -euo pipefail

readonly RED='\033[0;31m'
readonly GREEN='\033[0;32m'
readonly YELLOW='\033[1;33m'
readonly BLUE='\033[0;34m'
readonly NC='\033[0m'

log_info()    { echo -e "${BLUE}[INFO]${NC} $*"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $*"; }
log_warn()    { echo -e "${YELLOW}[WARN]${NC} $*"; }
log_error()   { echo -e "${RED}[ERROR]${NC} $*"; }

# Config
NGINX_ENABLED="${NGINX_ENABLED:-true}"
DOMAIN_SUFFIX="${DOMAIN_SUFFIX:-.home}"
PI_USER="${PI_USER:-piadmin}"

main() {
    log_info "Starting Nginx Reverse Proxy setup..."
    
    [[ "${NGINX_ENABLED}" != "true" ]] && { log_info "Nginx disabled in config"; return 0; }
    
    install_nginx
    resolve_port_conflicts
    configure_nginx
    test_and_restart
    show_dns_instructions
    
    log_success "Nginx Reverse Proxy setup completed!"
}

install_nginx() {
    log_info "Installing Nginx..."
    
    if command -v nginx >/dev/null 2>&1; then
        log_info "Nginx already installed"
    else
        apt-get update -qq
        DEBIAN_FRONTEND=noninteractive apt-get install -y -qq nginx
    fi
    
    systemctl enable nginx
}

resolve_port_conflicts() {
    log_info "Resolving port 80 conflicts (Pi-hole FTL)..."
    
    # Stop services temporarily
    systemctl stop pihole-FTL 2>/dev/null || true
    systemctl stop nginx 2>/dev/null || true
    systemctl stop lighttpd 2>/dev/null || true
    
    # Pi-hole v6 (TOML)
    local toml_conf="/etc/pihole/pihole.toml"
    local ftl_conf="/etc/pihole/pihole-FTL.conf"
    
    if [[ -f "${toml_conf}" ]]; then
        log_info "Found Pi-hole v6 config, setting web port to 8081..."
        if command -v pihole-FTL >/dev/null 2>&1; then
            pihole-FTL --config webserver.port 8081
        else
            # Manual TOML edit
            if grep -q "\[webserver\]" "${toml_conf}"; then
                sed -i 's/^port\s*=.*/port = 8081/' "${toml_conf}"
            else
                echo -e "\n[webserver]\n  port = 8081" >> "${toml_conf}"
            fi
        fi
        log_success "Pi-hole v6 web port set to 8081"
        
    elif [[ -f "${ftl_conf}" ]]; then
        log_info "Found Pi-hole v5 config, setting web port to 8081..."
        if grep -q "FTLCONF_webserver_port=" "${ftl_conf}"; then
            sed -i 's/^FTLCONF_webserver_port=.*/FTLCONF_webserver_port=8081/' "${ftl_conf}"
        else
            echo "FTLCONF_webserver_port=8081" >> "${ftl_conf}"
        fi
        log_success "Pi-hole v5 web port set to 8081"
        
    else
        # Fallback to lighttpd
        if [[ -f /etc/lighttpd/lighttpd.conf ]]; then
            log_info "Configuring lighttpd to port 8081..."
            sed -i -E 's/^server\.port\s*=\s*80/server.port = 8081/' /etc/lighttpd/lighttpd.conf
            [[ -f /etc/lighttpd/external.conf ]] && sed -i -E 's/server\.port\s*=\s*80/server.port = 8081/' /etc/lighttpd/external.conf
            log_success "Lighttpd moved to port 8081"
        fi
    fi
}

configure_nginx() {
    log_info "Creating Nginx configuration..."
    
    # Remove default site
    rm -f /etc/nginx/sites-enabled/default
    
    # Get local IP
    local ip
    ip=$(hostname -I | awk '{print $1}')
    
    # Create main reverse proxy config
    cat > /etc/nginx/sites-available/reverse-proxy.conf <<EOF
# Pi Server Setup - Nginx Reverse Proxy
# Generated by pi-server-setup v2

# Default catch-all
server {
    listen 80 default_server;
    listen [::]:80 default_server;
    server_name _;
    
    root /var/www/html;
    index index.html;
    
    location / {
        try_files \$uri \$uri/ =200;
    }
    
    # Status page
    location /status {
        return 200 '<html><body><h1>Pi Server Gateway</h1><p>Services are running.</p><ul>
<li><a href="http://dashboard.home">Dashboard (Cockpit)</a></li>
<li><a href="http://pi.home">Pi-hole</a></li>
<li><a href="http://n8n.home">n8n Automation</a></li>
<li><a href="http://webmin.home">Webmin</a></li>
<li><a href="http://pdf.home">Stirling PDF</a></li>
<li><a href="http://grafana.home">Grafana</a></li>
<li><a href="http://prometheus.home">Prometheus</a></li>
</ul></body></html>';
        add_header Content-Type text/html;
    }
}

