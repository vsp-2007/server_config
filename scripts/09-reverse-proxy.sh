test_and_restart() {
    log_info "Testing Nginx configuration..."
    
    if nginx -t; then
        log_success "Nginx configuration test passed"
    else
        log_error "Nginx configuration test failed"
        return 1
    fi
    
    log_info "Restarting services..."
    systemctl restart nginx
    
    # Restart Pi-hole FTL (it will use port 8081 now)
    systemctl restart pihole-FTL 2>/dev/null || true
    systemctl restart lighttpd 2>/dev/null || true
    
    # Verify
    sleep 3
    if systemctl is-active --quiet nginx; then
        log_success "Nginx running on port 80"
    else
        log_error "Nginx failed to start"
        systemctl status nginx --no-pager
        return 1
    fi
}

show_dns_instructions() {
    local ip
    ip=$(hostname -I | awk '{print $1}')
    
    cat <<EOF

${YELLOW}==============================================================${NC}
${YELLOW}  IMPORTANT: Configure Local DNS in Pi-hole${NC}
${YELLOW}==============================================================${NC}

For friendly domain names to work, add these records in Pi-hole:

1. Go to: http://${ip}:8081/admin/dns_records.php
   (or http://pi.${DOMAIN_SUFFIX}/admin/dns_records.php if already working)

2. Add the following Local DNS records (all pointing to ${ip}):
EOF
    
    local domains=("dashboard" "pi" "n8n" "webmin" "pdf" "grafana" "prometheus" "alertmanager" "stirling")
    for domain in "${domains[@]}"; do
        echo "   - ${domain}${DOMAIN_SUFFIX}  ->  ${ip}"
    done
    
    cat <<EOF

3. Save and apply.

Alternatively, run this command on the Pi:
  pihole -a hostrecord dashboard${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord pi${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord n8n${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord webmin${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord pdf${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord grafana${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord prometheus${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord alertmanager${DOMAIN_SUFFIX} ${ip}
  pihole -a hostrecord stirling${DOMAIN_SUFFIX} ${ip}

${YELLOW}==============================================================${NC}
EOF
}

